
public class Pair{
	private int first;
	private int second;
	
	public Pair(int first, int second){
		this.setFirst(first);
		this.setSecond(second);
	}

	public int getFirst() {
		return first;
	}

	public void setFirst(int first) {
		this.first = first;
	}

	public int getSecond() {
		return second;
	}

	public void setSecond(int second) {
		this.second = second;
	}
}
